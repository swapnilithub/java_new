public class concat {
    public static void main(String[] args) {
        String a="hey man";
        String n =" I am fine";
        //concatenation will do with + operator
        System.out.println(a+n);
        System.out.println(a+" "+n);
        //one more way is there method we used
        System.out.println(a.concat(n));
    }
}
